package com.civildefense.dto;

public class TrainDTO {

	private int no;
	private String date;
	private int zipcode_no;
	private int train_team_no;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getZipcode_no() {
		return zipcode_no;
	}
	public void setZipcode_no(int zipcode_no) {
		this.zipcode_no = zipcode_no;
	}
	public int getTrain_team_no() {
		return train_team_no;
	}
	public void setTrain_team_no(int train_team_no) {
		this.train_team_no = train_team_no;
	}
	
	
}
